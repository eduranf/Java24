class PlanetWeight {
    public static void main(String[] args) {
        System.out.print("Your weight on Earth is ");
        double weight = 85;
        System.out.println(weight);
        
        System.out.print("Your weigth on Mercury is ");
        double mercury = weight * .378;
        System.out.println(mercury);
        
        System.out.print("Your weigth on the Moon is ");
        double moon = weight * .166;
        System.out.println(moon);
        
        System.out.print("Your weigth on Jupiter is ");
        double jupiter = weight * 2.364;
        System.out.println(jupiter);
        
        System.out.print("Your weigth on Venus is ");
        double venus = weight * 0.907;
        System.out.println(venus);
        
        System.out.print("Your weigth on Uranus is ");
        double uranus = weight * 0.889;
        System.out.println(uranus);
    }
}